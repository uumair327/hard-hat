  // In Godot, Y is UP. In our Flutter game, Y is DOWN.
  // Godot ground is at y=11 (from GridMap).
  // We need to invert Y and offset.

  // Sort by y then x
  tiles.sort((a, b) {
    final yCmp = (a['y'] as int).compareTo(b['y'] as int);
    if (yCmp != 0) return yCmp;
    return (a['x'] as int).compareTo(b['x'] as int);
  });

  // Print Dart level data
  print('// === LEVEL 1 TILE DATA (from Godot GridMap) ===');
  print('// Godot coord system: Y-up. Tile at godot(x, y)');
