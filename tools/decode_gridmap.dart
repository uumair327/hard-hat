String _flutterType(String godotName) {
  switch (godotName) {
    case 'scaffolding':
      return 'scaffolding';
    case 'timber':
      return 'timber';
    case 'timber_one_hit':
      return 'timberOneHit';
    case 'bricks':
      return 'bricks';
    case 'bricks_one_hit':
      return 'bricksOneHit';
    case 'bricks_two_hits':
      return 'bricksTwoHit';
    case 'girder':
      return 'beam'; // girder = indestructible beam
    default:
      return 'scaffolding';
  }
